                        <li>Tripolares Rotativos de 2 y 3 columnas, tipo SE.</li>
                    </ul>

                    <p>El conjunto está montado y completamente contenido en gabinete estanco de acero reforzado con
                        perfiles, pintado al horno, con respaldo de montaje y collar de anclaje y tiene un mecanismo de
                        desacople para la acción manual.</p>

                    <p>Consta de un motor reversible de corriente continua, una caja reductora, el pack de baterías de
                        electrolito absorbido y el Centro de Control Seccionador CCS-1B compuesto por: Unidad Central de
                        Proceso, Placa de Alimentación, Protecciones y Control de las Baterías, Placa de Potencia para
                        la regulación Tiempo / Corriente del Motor (programable mediante el puerto RS-232) y
                        comunicación DNP 3.0 o Modbus, que permite su incorporación a cualquier sistema SCADA disponible
                        y normalizado con estos protocolos.</p>

                    <p>El Centro de Control del Seccionador comanda el motor, que mueve la caja reductora, la cual
                        transmite su movimiento a los seccionadores que se citan precedentemente.</p>

                    <p>El mismo recibe información de dos fines de carrera que indican si el equipo está Abierto o
                        Cerrado y posee cuatro entradas analógicas disponibles que, mediante transductores opcionales,
                        permiten medir tres corrientes de fase y una tensión de línea.</p>

                    <p>Esta particularidad posibilita al operador de red conocer el estado de carga de los alimentadores
                        o transformadores vinculados a seccionadores asistidos por nuestros Comandos Motorizados CMSBC.
                    </p>

                    <p>Así, con equipos integrados al sistema Scada, se podría aportar información valiosa para el
                        gerenciamiento en tiempo real de una red.</p>

                    <p>Utilizando las tres entradas digitales disponibles, es posible tomar señal de detectores de falla
                        que, ubicados uno por fase, son una opción válida para detectar fallas sobre el puesto de
                        seccionamiento telecomandado que, al ser parte de un sistema anillado asistido por SCADA,
                        permite aislar en forma automática al segmento en falla, mientras el resto del anillo permanece
                        en servicio normal.</p>

                    <p>El control CCS-1B maneja las corrientes de arranque y marcha del motor con dispositivos de estado
                        sólido, logrando un arranque óptimo del mismo y administrando la maniobra en potencia y tiempo,
                        logrando así un excelente guardamotor electrónico.</p>

                    <p>Un cargador de baterías, de tres estados, forma parte de este controlador manteniendo el nivel de
                        carga de las baterías y prolongando su vida útil.</p>

                    <p>El panel frontal tiene la señalización necesaria para controlar al equipo, dos pulsadores
                        posibilitan la apertura y cierre eléctrico local, una llave permite optar entre el accionamiento
                        LOCAL o REMOTO y un pulsador para realizar el TEST de BATERIA conforman la parte operativa de
                        esta unidad.</p>

                    <p>Cuenta con indicadores de alta luminosidad, que informan sobre el estado ABIERTO o CERRADO del
                        seccionador e indicación del equipo predispuesto para el accionamiento LOCAL o REMOTO.</p>

